const pipe = (...fns) =>
	res =>
		fns.reduce((acc, next) => next(acc), res)


const splitBy = cond => xs =>
  xs.reduce(
    (acc, cur) => {
      return cond(cur)
        ? {
            ...acc,
            left: [...acc.left, cur],
          }
        : {
            ...acc,
            right: [...acc.right, cur],
          };
    },
    {
      left: [],
      right: [],
    }
  );

const counts = key => xs => ({
  counts: xs.reduce((acc, cur) => {
    if (!acc[cur[key]]) {
      acc[cur[key]] = 0;
    }

    acc[cur[key]] = acc[cur[key]] + 1;

    return acc;
  }, {}),
  total: xs.length,
});

const relativeCounts = ({ counts, total }) =>
  Object.entries(counts).reduce(
    (acc, [key, count]) => ({
      ...acc,
      [key]: count / total,
    }),
    {}
  );

const map = f => xs => xs.map(f)
const filter = p => xs => xs.filter(p)
const sum = xs => xs.reduce((acc, cur) => acc + cur, 0)

